### Repetition Structures also knnown as loops
## while condition: (test for T/F value)
##    stmt (repeat as long as condition is true)
##    ....
##    stmt
##    

count = 0
while count < 3:
    print("Hello")
    count = count + 1

print("Round 1")

count = 0
while count < 5:
    print("Hello, have a nice day!")
    count += 1

print("Round 2")


count = 5
while count > 0:
    print("Hello, have a nice day!")
    count -= 1

print("Bye")
